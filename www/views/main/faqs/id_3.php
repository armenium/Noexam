<strong>Traditional Term</strong>

<p>Term Insurance designed to cover you for specific periods of time remains the most competitive and affordable form of coverage. If you are young or in excellent health, you may purchase a “no-exam” policy for immediate coverage, but you may want to apply for a traditional term policy as well. </p>

<p>For young and healthy individuals, a traditionally underwritten policy is usually more affordable. The coverage options may even expand with larger terms and more available riders to tailor the policies to your needs. </p>

<strong>Permanent Insurance</strong>

<p>Permanent Insurance is a life product with no expiration date like term insurance. You will be covered until the end of your life. Permanent policies provide a wide range of flexibility because they are not just a life policy, but an investment product.</p>

<p>Within permanent insurance a portion of your premium is used to create a bank of cash value. That bank of cash continues to grow during your lifetime, and gives you options you will not have with a term policy. When the accrual of cash value reaches a certain limit, you can use the cash to pay the premiums on the policy for the remainder of your life.</p>

<p>You can also use the cash value to finance other expenses you may encounter during your life. It’s not uncommon for clients to borrow against the cash on their policy to fund college tuition, home purchases or even business ventures. The options are unlimited on how you can use the cash. In a sense, you become your own banker. You borrow from your store of cash, but pay yourself back continuing to accrue value over time. </p>
